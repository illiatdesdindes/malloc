char	*ft_strcat(char *s1, const char *s2)
{
	int	i;
	int	s1_end;

	i = 0;
	while (s1[i])
		i++;
	s1_end = i;
	while (s2[i - s1_end])
	{
		s1[i] = s2[i - s1_end];
		i++;
	}
	s1[i] = '\0';
	return (s1);
}
